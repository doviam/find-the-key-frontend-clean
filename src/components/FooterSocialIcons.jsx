function SocialIcon({ label, className }) {
  const cls = `${className} text-white`;
  switch (label) {
    case "Instagram":
      return <InstagramIcon className={cls} />;
    case "Spotify":
      return <SpotifyIcon className={cls} />;
    case "TikTok":
      return <TikTokIcon className={cls} />;
    case "YouTube":
      return <YouTubeIcon className={cls} />;
    case "Twitch":
      return <TwitchIcon className={cls} />;
    default:
      return null;
  }
}

export function FooterSocialIcons() {
  const iconBase = "block h-5 w-5 shrink-0 transition group-hover:opacity-100 sm:h-6 sm:w-6";

  return (
    <ul className="m-0 flex list-none flex-wrap items-center justify-center gap-4 sm:gap-5 p-0" aria-label="Redes sociales">
      {FOOTER_SOCIAL_LINKS.map((item) => {
        const isInternal = item.href.startsWith("/") && !item.href.startsWith("//");
        const content = <SocialIcon label={item.label} className={iconBase} />;
        const className =
          "group block leading-none outline-none ring-accent/30 transition opacity-90 hover:opacity-100 focus-visible:ring-2";
        return (
          <li key={item.label}>
            {isInternal ? (
              <Link to={item.href} className={className} aria-label={item.label}>
                {content}
              </Link>
            ) : (
              <a
                href={item.href}
                className={className}
                aria-label={item.label}
                target="_blank"
                rel="noopener noreferrer"
              >
                {content}
              </a>
            )}
          </li>
        );
      })}
    </ul>
  );
}
